export const DAYS = Array.from({ length: 31 }, (_, i) => (i + 1).toString());

export function getYearOptions(): string[] {
  const currentYear = new Date().getFullYear();
  return Array.from({ length: currentYear - 1899 }, (_, i) =>
    (currentYear - i).toString(),
  );
}
