// Returns the normalized URL string only if it parses cleanly and uses an
// http(s) scheme. Blocks javascript:, data:, vbscript:, file:, etc., which
// would otherwise execute when placed in an href.
export function safeHttpUrl(value: string | null | undefined): string | null {
  if (!value) return null;
  try {
    const url = new URL(value);
    if (url.protocol !== "http:" && url.protocol !== "https:") return null;
    return url.toString();
  } catch {
    return null;
  }
}
